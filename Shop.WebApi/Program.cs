using Shop.Application;
using Shop.Infrastructure.Dapper;
using Shop.Infrastructure.EfCore;
using Shop.Infrastructure.EfCore.Common;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

#region ServicesRegistration

builder.Services.AddInfrastructureEfCoreServices(builder.Configuration);
builder.Services.AddInfrastructureDapperServices();
builder.Services.AddApplicationServices(builder.Configuration);

#endregion

var app = builder.Build();

using (var scope = app.Services.CreateScope())
{
    var context = scope.ServiceProvider.GetRequiredService<AppDbContext>();
    context.Database.EnsureCreated();
}

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthentication();
app.UseAuthorization();

app.MapControllers();

app.Run();